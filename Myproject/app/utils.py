import re

LETTER_PATTERN = re.compile(r'^[^A-Za-zА-Яа-я]+')


def clean_reference_line(text: str) -> str:
    """
    Очистка одной строки библиографической ссылки:
    1) удаление всех символов и пробелов до первой буквы;
    2) удаление пробелов в конце строки.
    Внутренние пробелы между словами не трогаем.
    """
    if text is None:
        return ""

    # Преобразуем к строке на всякий случай
    value = str(text)

    # 1. Удалить все символы до первой буквы (цифры, точки, табы, пробелы и т.п.)
    # Например: "1.\t  Антонов..." -> "Антонов..."
    value = LETTER_PATTERN.sub("", value)

    # 2. Удалить пробелы и табы в конце строки
    value = value.rstrip()

    return value
